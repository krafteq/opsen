package ingress

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strings"
)

// CaddyDriver generates Caddyfile blocks.
type CaddyDriver struct {
	configDir string
	reloadCmd string
}

func (d *CaddyDriver) WriteConfig(clientName string, routes []Route) error {
	var lines []string
	lines = append(lines, fmt.Sprintf("# Auto-generated by opsen-agent for client: %s", clientName))
	lines = append(lines, "# DO NOT EDIT")
	lines = append(lines, "")

	for _, route := range routes {
		for _, host := range route.Hosts {
			lines = append(lines, fmt.Sprintf("%s {", host))

			// Security headers
			lines = append(lines, "  header {")
			lines = append(lines, "    Strict-Transport-Security \"max-age=31536000; includeSubDomains\"")
			lines = append(lines, "    X-Content-Type-Options nosniff")
			lines = append(lines, "    X-Frame-Options DENY")
			lines = append(lines, "    Referrer-Policy strict-origin-when-cross-origin")
			lines = append(lines, "  }")

			// Custom headers
			for k, v := range route.Headers {
				lines = append(lines, fmt.Sprintf("  header %s \"%s\"", k, v))
			}

			// Rate limiting
			if route.RateLimitRps > 0 {
				lines = append(lines, fmt.Sprintf("  rate_limit {static %d %d}", route.RateLimitRps, route.RateLimitRps*2))
			}

			// CORS
			if route.CORS != nil && len(route.CORS.Origins) > 0 {
				lines = append(lines, "  @cors method OPTIONS")
				lines = append(lines, fmt.Sprintf("  header @cors Access-Control-Allow-Origin \"%s\"", strings.Join(route.CORS.Origins, ", ")))
				if len(route.CORS.Methods) > 0 {
					lines = append(lines, fmt.Sprintf("  header @cors Access-Control-Allow-Methods \"%s\"", strings.Join(route.CORS.Methods, ", ")))
				}
				lines = append(lines, "  header @cors Access-Control-Allow-Headers \"Content-Type, Authorization\"")
			}

			// Reverse proxy
			upstream := route.Upstream
			if route.PathPrefix != "" && route.PathPrefix != "/" {
				lines = append(lines, fmt.Sprintf("  handle_path %s* {", route.PathPrefix))
				lines = append(lines, fmt.Sprintf("    reverse_proxy %s", upstream))
				lines = append(lines, "  }")
			} else {
				lines = append(lines, fmt.Sprintf("  reverse_proxy %s", upstream))
			}

			lines = append(lines, "}")
			lines = append(lines, "")
		}
	}

	path := filepath.Join(d.configDir, clientName+".conf")
	return os.WriteFile(path, []byte(strings.Join(lines, "\n")), 0644)
}

func (d *CaddyDriver) DeleteRoute(clientName string, routeName string) error {
	path := filepath.Join(d.configDir, clientName+".conf")
	return os.Remove(path)
}

func (d *CaddyDriver) ListRoutes(clientName string) ([]string, error) {
	path := filepath.Join(d.configDir, clientName+".conf")
	data, err := os.ReadFile(path)
	if err != nil {
		if os.IsNotExist(err) {
			return []string{}, nil
		}
		return nil, err
	}

	var routes []string
	for _, line := range strings.Split(string(data), "\n") {
		// Only match top-level blocks (not indented) — domain names start at column 0
		if len(line) > 0 && line[0] != ' ' && line[0] != '\t' && line[0] != '#' {
			trimmed := strings.TrimSpace(line)
			if strings.HasSuffix(trimmed, " {") {
				routes = append(routes, strings.TrimSuffix(trimmed, " {"))
			}
		}
	}
	return routes, nil
}

func (d *CaddyDriver) Reload() error {
	if d.reloadCmd == "" {
		return nil
	}

	parts := strings.Fields(d.reloadCmd)
	cmd := exec.Command(parts[0], parts[1:]...)
	output, err := cmd.CombinedOutput()
	if err != nil {
		return fmt.Errorf("reload failed: %s: %s", err, string(output))
	}
	return nil
}
